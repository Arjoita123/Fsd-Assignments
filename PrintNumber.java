class PrintNumber{
	public static void main(String[] args) {
		
		for(int i=35;i<47;i++){
			if(i==38){
				System.out.println("Thirty Eight");
				continue;
			}
			if(i==40){
				continue;
			}
			if(i==45){
				System.out.println("forty five");
				continue;
			}
			
			System.out.println(i);
		}
		
			
		
		
	}
}